fn main() {
	
	let num_1 = 10;
	let num_2 = 5;

	let hasil_pertambahan = num_1 + num_2;
	let hasil_pengurangan = num_1 - num_2;
	let hasil_perkalian   = num_1 * num_2;
	let hasil_pembagaian  = num_1 / num_2;
	let hasil_modulus     = num_1 % num_2;
	
	println!("Hasil Pertambahan ->> {} ", hasil_pertambahan);
	println!("Hasil Pengurangan ->> {} ", hasil_pengurangan);
	println!("Hasil Perkalian   ->> {} ", hasil_perkalian);
	println!("Hasil Pembagian   ->> {} ", hasil_pembagaian);
	println!("Hasil Modulus     ->> {} ", hasil_modulus);
}
